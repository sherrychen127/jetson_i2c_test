int get_filedescriptor(){
	char fileNameBuffer[32];
	sprintf(fileNameBuffer, "/dev/i2c-0");
	int FileDescriptor = open(fileNameBuffer, 2);
	printf("%d\n", FileDescriptor);
	if (FileDescriptor <0){
		return -1;
	}
	if (ioctl(FileDescriptor, I2C_SLAVE, 0x3c) <0){
		return -1;
	}
	return FileDescriptor;
}

int i2cwrite(int writeValue){
	int FileDescriptor = get_filedescriptor();
	int toReturn = i2c_smbus_write_byte(FileDescriptor,writeValue);
	if (toReturn<0){
		return -1;
	}
	return toReturn;
}

